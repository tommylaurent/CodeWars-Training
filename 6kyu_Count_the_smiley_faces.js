//! utiliser .match pour comparer sa regex à un élément

/******************************************************************************************/
//* EXERCICE
/******************************************************************************************/

function countSmileys(arr) {
  const regex = /((:|;)(-|~|)(\)|D))/g;
  return arr.filter(elt => elt.match(regex)).length;
}

const test = countSmileys([":)", ":(", ":D", ":O", ":;"]);
